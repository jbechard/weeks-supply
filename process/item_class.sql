SET NOCOUNT ON
SET TRANSACTION ISOLATION LEVEL READ UNCOMMITTED

SELECT itm.ITEM,
  CASE WHEN c.ITEM IS NOT NULL THEN c.CLASS 
  ELSE CASE WHEN itm.SELECTION_CD IN('R','G') THEN 'A' ELSE 'Seasonal' END
  END as CLASS
FROM ITEM itm 
LEFT JOIN ufn_EVERYDAY_ITEM_CLASS() c ON c.ITEM=itm.ITEM
WHERE item_grp IN(301,302,303,304,305,306,307,308,309,310,311,312,313,314,315,389,390)
